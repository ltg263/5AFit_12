package com.jxkj.fit_5a.entity;

public class RatePatternBean {


    String name;
    String v;

    public RatePatternBean(String name, String v) {
        this.name = name;
        this.v = v;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getV() {
        return v;
    }

    public void setV(String v) {
        this.v = v;
    }
}
