package com.jxkj.fit_5a.entity;

public class ViewPagerTopBean {
    String title;
    boolean isSelect;

    public ViewPagerTopBean(String title, boolean isSelect) {
        this.title = title;
        this.isSelect = isSelect;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public boolean isSelect() {
        return isSelect;
    }

    public void setSelect(boolean select) {
        isSelect = select;
    }
}
