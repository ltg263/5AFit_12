package com.jxkj.fit_5a.entity;

public class SignatureBean {

    /**
     * accessid :
     * callback :
     * dir :
     * expireEndTimestamp : 0
     * host :
     * policy :
     * signature :
     */

    private String accessid;
    private String callback;
    private String dir;
    private String expireEndTimestamp;
    private String host;
    private String policy;
    private String signature;

    public String getAccessid() {
        return accessid;
    }

    public void setAccessid(String accessid) {
        this.accessid = accessid;
    }

    public String getCallback() {
        return callback;
    }

    public void setCallback(String callback) {
        this.callback = callback;
    }

    public String getDir() {
        return dir;
    }

    public void setDir(String dir) {
        this.dir = dir;
    }

    public String getExpireEndTimestamp() {
        return expireEndTimestamp;
    }

    public void setExpireEndTimestamp(String expireEndTimestamp) {
        this.expireEndTimestamp = expireEndTimestamp;
    }

    public String getHost() {
        return host;
    }

    public void setHost(String host) {
        this.host = host;
    }

    public String getPolicy() {
        return policy;
    }

    public void setPolicy(String policy) {
        this.policy = policy;
    }

    public String getSignature() {
        return signature;
    }

    public void setSignature(String signature) {
        this.signature = signature;
    }
}
